import {
  useEffect,
  useState
} from "react"

import {
  useNavigate
} from "react-router-dom"

import AuthModel
  from "./AuthModel"

const ProtectedRoute = ({
  children
}) => {

  /*
  |--------------------------------------------------------------------------
  | Navigation
  |--------------------------------------------------------------------------
  */

  const navigate =
    useNavigate()

  /*
  |--------------------------------------------------------------------------
  | Model State
  |--------------------------------------------------------------------------
  */

  const [showModel,
    setShowModel] =
      useState(false)

  /*
  |--------------------------------------------------------------------------
  | Token
  |--------------------------------------------------------------------------
  */

  const token =
    localStorage.getItem(
      "token"
    )

  /*
  |--------------------------------------------------------------------------
  | Open Model If Not Logged In
  |--------------------------------------------------------------------------
  */

  useEffect(() => {

    if (!token) {

      setShowModel(true)
    }

  }, [token])

  /*
  |--------------------------------------------------------------------------
  | Close Model
  |--------------------------------------------------------------------------
  */

  const handleClose = () => {

    setShowModel(false)

    /*
    |--------------------------------------------------------------------------
    | Redirect Home
    |--------------------------------------------------------------------------
    */

    navigate("/")
  }

  /*
  |--------------------------------------------------------------------------
  | Not Logged In
  |--------------------------------------------------------------------------
  */

  if (!token) {

    return (

      <AuthModel
        isOpen={showModel}
        onClose={handleClose}
      />
    )
  }

  /*
  |--------------------------------------------------------------------------
  | Logged In
  |--------------------------------------------------------------------------
  */

  return children
}

export default ProtectedRoute